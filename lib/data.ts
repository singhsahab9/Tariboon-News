export function getPostBySlug(slug: string): NewsPost | undefined {
  return posts.find((post) => post.slug === slug)
}

export function getPostsByCategory(categorySlug: string): NewsPost[] {
  return posts.filter((post) => post.categorySlug === categorySlug)
}

export function getFeaturedPosts(): NewsPost[] {
  return posts.filter((post) => post.featured)
}

export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find((cat) => cat.slug === slug)
}
