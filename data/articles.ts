export function getArticleBySlug(slug: string): Article | undefined {
  return articles.find((article) => article.slug === slug)
}

export function getArticlesByCategory(categorySlug: string): Article[] {
  return articles.filter((article) => article.categorySlug === categorySlug)
}

export function getArticlesByTag(tagSlug: string): Article[] {
  return articles.filter((article) => article.tags.some((tag) => tag.slug === tagSlug))
}

export function getArticlesByAuthor(authorId: string): Article[] {
  return articles.filter((article) => article.author.id === authorId)
}

export function getFeaturedArticles(): Article[] {
  return articles.filter((article) => article.featured)
}

export function getBreakingNews(): Article[] {
  return articles.filter((article) => article.breaking)
}

export function getLatestArticles(count: number = 10): Article[] {
  return articles.slice(0, count)
}

export function getPopularArticles(count: number = 10): Article[] {
  return [...articles].sort((a, b) => b.views - a.views).slice(0, count)
}

export function getRelatedArticles(articleId: string, count: number = 5): Article[] {
  const article = articles.find((a) => a.id === articleId)
  if (!article) return []
  return articles.filter(
    (a) => a.id !== articleId && (a.categorySlug === article.categorySlug || a.tags.some((t) => article.tags.some((at) => at.slug === t.slug)))
  ).slice(0, count)
}